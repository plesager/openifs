          DO ILEV=1,STRATO_CMIP_NALT
            JLEV=STRATO_CMIP_NALT-ILEV+1
            DO ILAT=1,STRATO_CMIP_NLAT
              ! SW parameters
              DO IB=1,STRATO_CMIP_NSB
                ! We keep three months to make a smoothed forcing (linear interpolation along the axis time)           
                DO IMONTH=1,STRATO_CMIP_NMONTH

                  AOD_SUN(ILEV,ILAT,IB,IMONTH)=AOD_SUN_ORIG(IB,ILAT,JLEV,IMON-2+IMONTH)
                  AAOD_SUN(ILEV,ILAT,IB,IMONTH)=AAOD_SUN_ORIG(IB,ILAT,JLEV,IMON-2+IMONTH)
                  REFAOD_SUN(ILEV,ILAT,IB,IMONTH)=REFAOD_SUN_ORIG(IB,ILAT,JLEV,IMON-2+IMONTH)

                ENDDO
              ENDDO
              ! LW parameter
              DO IB=1,STRATO_CMIP_NTB
                DO IMONTH=1,STRATO_CMIP_NMONTH
                  AAOD_EARTH(ILEV,ILAT,IB,IMONTH)=AAOD_EARTH_ORIG(IB,ILAT,JLEV,IMON-2+IMONTH)
                ENDDO
              ENDDO
            ENDDO
          ENDDO
        ENDIF !IMON
        DEALLOCATE(AOD_SUN_ORIG   )
        DEALLOCATE(AAOD_SUN_ORIG  )
        DEALLOCATE(REFAOD_SUN_ORIG)
        DEALLOCATE(AAOD_EARTH_ORIG)
      ENDIF CMIP
    ENDIF AVAIL
  ENDIF ROOT
  
  CALL BROADCAST_AER_CMIP_PROP(YDMODEL)
  LSTRATAERO_UPDATED = .TRUE.

ELSE

  !DBG WRITE(NULOUT,*)'Same month in CMIP_STRATO_AERO -> nothing to update '
  LSTRATAERO_UPDATED = .FALSE.

ENDIF NEWMONTH

END ASSOCIATE
END ASSOCIATE
END ASSOCIATE
END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('CMIP_STRATO_AERO',1,ZHOOK_HANDLE)

