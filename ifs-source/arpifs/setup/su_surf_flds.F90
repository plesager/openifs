    CALL SETUP_SFLP2(YSD_VXD,YSD_VX%YARG,  CDNAME='SURFPROP.ARGILE ')
    YSD_VX%YRSMIN => YSD_VX%YVX(YSD_VXD%IPTR)
    CALL SETUP_SFLP2(YSD_VXD,YSD_VX%YRSMIN,CDNAME='SURFRESI.STO.MIN')
    YSD_VX%YLAI   => YSD_VX%YVX(YSD_VXD%IPTR)
    CALL SETUP_SFLP2(YSD_VXD,YSD_VX%YLAI,  CDNAME='SURFIND.FOLIAIRE')
    YSD_VX%YVEG   => YSD_VX%YVX(YSD_VXD%IPTR)
    CALL SETUP_SFLP2(YSD_VXD,YSD_VX%YVEG,  CDNAME='SURFPROP.VEGETAT')
  ENDIF
ENDIF

! - Set information that couldn't be set until the total number of VCLIX fields was known.
CALL FINALISE_SFLP2(YDDYNA,YDSURF,YSD_VXD,YSD_VX%YVX,LDVERBOSE=LLVERBOSE)

! * Group DI= Phys DIagnostics


IF (ASSOCIATED(YSD_DI%YXA)) DEALLOCATE(YSD_DI%YXA)
IVEXTRDI=0
IF (LEDR) THEN
  IVEXTRDI=IVEXTRDI+1
  IF (IVEXTRDI > JPVEXTRDI) CALL ABOR1('SU_SURF_FLDS : JPVEXTRDI TOO SMALL')
  CVEXTRDI(IVEXTRDI)='EDR             '
  NVEXTRDIGB(IVEXTRDI)=136
  VEXTRDI(IVEXTRDI)=0.0_JPRB
ENDIF
! One can add more fields here. Don't forget individual pointers (to be created first) after the finalization
NVEXTRDI=IVEXTRDI
ALLOCATE(YSD_DI%YXA(NVEXTRDI))
IF (NVEXTRDI > 0) THEN
  ICEXTR=NFLEVG
ELSE
  ICEXTR=0
ENDIF
ALLOCATE(CLNAMELEVS(ICEXTR))
CALL INI_SFLP3(YSD_DID,YSD_DI%YXA,ICEXTR,.FALSE.,'VEXTRDI - SD_DI  ')
IF(NVEXTRDI> 0) THEN
  ALLOCATE(IREQIN_TMP(YSD_DID%NLEVS))
  IF (LREQIN_VEXTRDI) THEN
    IREQIN_TMP(:)=1
  ELSE
    IREQIN_TMP(:)=-1
  ENDIF
  DO JFLD=1,NVEXTRDI
    DO JLEV=1,YSD_DID%NLEVS
      WRITE(UNIT=CLNAMELEVS(JLEV),FMT='(''S'',I3.3,A12)') JLEV,CVEXTRDI(JFLD)
    ENDDO
    CALL SETUP_SFLP3(YSD_DID,YSD_DI%YXA(JFLD),&
     & KGRIB   =SPREAD(NVEXTRDIGB(JFLD),1,YSD_DID%NLEVS),&
     & CDNAME  =CLNAMELEVS(1:YSD_DID%NLEVS),&
     & KREQIN  =IREQIN_TMP(1:YSD_DID%NLEVS),&
     & PDEFAULT=SPREAD(VEXTRDI(JFLD) ,1,YSD_DID%NLEVS))
  ENDDO
  DEALLOCATE(IREQIN_TMP)
ENDIF
DEALLOCATE(CLNAMELEVS)
CALL FINALISE_SFLP3(YDDYNA,YDSURF,YSD_DID,YSD_DI%YXA,LDVERBOSE=LLVERBOSE)
IVEXTRDI=0
IF (LEDR) THEN
  IVEXTRDI=IVEXTRDI+1
  YSD_DI%YXEDR => YSD_DI%YXA(IVEXTRDI)
ELSE
  YSD_DI%YXEDR => NULL()
ENDIF

! * Group XP=VPRECIP

IF(ASSOCIATED(YSD_XP%YXP1)) DEALLOCATE(YSD_XP%YXP1)
IF (LDPRECIPS) THEN
   IVPRECIP=1
   ILEVS=NDTPREC
ELSE
   IVPRECIP=0
   ILEVS=0
ENDIF
ALLOCATE(YSD_XP%YXP1(IVPRECIP))
CALL INI_SFLP3(YSD_XPD,YSD_XP%YXP1,ILEVS,.FALSE.,'VPRECIP - SD_XP  ')

IF(IVPRECIP > 0) THEN
  DO JFLD=1,IVPRECIP
    CALL SETUP_SFLP3(YSD_XPD,YSD_XP%YXP1(JFLD),&
     & KGRIB   =SPREAD(NVPRECIPGB(JFLD),1,YSD_XPD%NLEVS),&
     & CDNAME  =SPREAD(CVPRECIP(JFLD)  ,1,YSD_XPD%NLEVS),&
     & PDEFAULT=SPREAD(VPRECIP(JFLD) ,1,YSD_XPD%NLEVS))
  ENDDO
ENDIF
CALL FINALISE_SFLP3(YDDYNA,YDSURF,YSD_XPD,YSD_XP%YXP1,LDVERBOSE=LLVERBOSE)
IF (LDPRECIPS) THEN
  YSD_XP%YPRECIP => YSD_XP%YXP1(IVPRECIP)
ELSE
  YSD_XP%YPRECIP => NULL()
ENDIF


! * Group XP=VPRECIP2

IF(ASSOCIATED(YSD_XP2%YXP2)) DEALLOCATE(YSD_XP2%YXP2)
IF (LDPRECIPS2) THEN
   IVPRECIP2=1
   ILEVS=NDTPREC2
ELSE
   IVPRECIP2=0
   ILEVS=0
ENDIF
ALLOCATE(YSD_XP2%YXP2(IVPRECIP2))
CALL INI_SFLP3(YSD_XP2D,YSD_XP2%YXP2,ILEVS,.FALSE.,'VPRECIP2 - SD_XP2  ')

IF(IVPRECIP2 > 0) THEN
  DO JFLD=1,IVPRECIP2
    CALL SETUP_SFLP3(YSD_XP2D,YSD_XP2%YXP2(JFLD),&
     & KGRIB   =SPREAD(NVPRECIPGB2(JFLD),1,YSD_XP2D%NLEVS),&
     & CDNAME  =SPREAD(CVPRECIP2(JFLD)  ,1,YSD_XP2D%NLEVS),&
     & PDEFAULT=SPREAD(VPRECIP2(JFLD) ,1,YSD_XP2D%NLEVS))
  ENDDO
ENDIF
CALL FINALISE_SFLP3(YDDYNA,YDSURF,YSD_XP2D,YSD_XP2%YXP2,LDVERBOSE=LLVERBOSE)
IF (LDPRECIPS2) THEN
  YSD_XP2%YPRECIP2 => YSD_XP2%YXP2(IVPRECIP2)
ELSE
  YSD_XP2%YPRECIP2 => NULL()
ENDIF


! * Group XA=VEXTRA

IF(ASSOCIATED(YSD_XA%YXA)) DEALLOCATE(YSD_XA%YXA)
ALLOCATE(YSD_XA%YXA(NVEXTR))
CALL INI_SFLP3(YSD_XAD,YSD_XA%YXA,NCEXTR,.FALSE.,'VEXTRA - SD_XA  ')
IF(NVEXTR > 0) THEN
  DO JFLD=1,NVEXTR
    CALL SETUP_SFLP3(YSD_XAD,YSD_XA%YXA(JFLD),&
     & KGRIB   =SPREAD(NVEXTRAGB(JFLD),1,YSD_XAD%NLEVS),&
     & CDNAME  =SPREAD(CVEXTRA(JFLD)  ,1,YSD_XAD%NLEVS),&
     & PDEFAULT=SPREAD(VEXTRADF(JFLD) ,1,YSD_XAD%NLEVS))
  ENDDO
ENDIF
CALL FINALISE_SFLP3(YDDYNA,YDSURF,YSD_XAD,YSD_XA%YXA,LDVERBOSE=LLVERBOSE)

! * Group PF= precip fraction

IF(ASSOCIATED(YSD_PF%YXA)) DEALLOCATE(YSD_PF%YXA)
ALLOCATE(YSD_PF%YXA(NVECOUT))
CALL INI_SFLP3(YSD_PFD,YSD_PF%YXA,NFLEVG,.FALSE.,'precip fraction - SD_PF  ')!???
IF(NVECOUT > 0) THEN
  DO JFLD=1,NVECOUT
     CALL SETUP_SFLP3(YSD_PFD,YSD_PF%YXA(JFLD),&
     & KGRIB   =SPREAD(NVEXTRAGB(JFLD),1,YSD_PFD%NLEVS),&
     & CDNAME  =SPREAD(CVEXTRA(JFLD)  ,1,YSD_PFD%NLEVS),&
     & PDEFAULT=SPREAD(VEXTRADF(JFLD) ,1,YSD_PFD%NLEVS))
  ENDDO
ENDIF
CALL FINALISE_SFLP3(YDDYNA,YDSURF,YSD_PFD,YSD_PF%YXA,LDVERBOSE=.FALSE.)

! * Group XR=VEXTRA-radiation

IF(ASSOCIATED(YSD_XR%YXA)) DEALLOCATE(YSD_XR%YXA)
ALLOCATE(YSD_XR%YXA(NVEXTRRAD))
IF (NVEXTRRAD > 0) THEN
  ICEXTR=NFLEVG
ELSE
  ICEXTR=0
ENDIF
CALL INI_SFLP3(YSD_XRD,YSD_XR%YXA,ICEXTR,.FALSE.,'VEXTRAD - SD_XR  ')
IF(NVEXTRRAD> 0) THEN
  DO JFLD=1,NVEXTRRAD
    CALL SETUP_SFLP3(YSD_XRD,YSD_XR%YXA(JFLD),&
     & KGRIB   =SPREAD(NVEXRADGB(JFLD),1,YSD_XRD%NLEVS),&
     & CDNAME  =SPREAD(CVEXRAD(JFLD)  ,1,YSD_XRD%NLEVS),&
     & PDEFAULT=SPREAD(VEXRADDF(JFLD) ,1,YSD_XRD%NLEVS))
  ENDDO
ENDIF
CALL FINALISE_SFLP3(YDDYNA,YDSURF,YSD_XRD,YSD_XR%YXA,LDVERBOSE=LLVERBOSE)

! * Group X2=VEXTR2

IF(ASSOCIATED(YSD_X2%YX2)) DEALLOCATE(YSD_X2%YX2)
ALLOCATE(YSD_X2%YX2(NVXTR2))
CALL INI_SFLP2(YSD_X2D,YSD_X2%YX2,.FALSE.,'VEXTR2 - SD_X2  ')
IF(NVXTR2 > 0) THEN
  DO JFLD=1,NVXTR2
    CALL SETUP_SFLP2(YSD_X2D,YSD_X2%YX2(JFLD),&
     & KGRIB   =NVEXTR2GB(JFLD),&
     & CDNAME  =CVEXTR2(JFLD)  ,&
     & PDEFAULT=VEXTR2DF(JFLD) )
  ENDDO
ENDIF
CALL FINALISE_SFLP2(YDDYNA,YDSURF,YSD_X2D,YSD_X2%YX2,LDVERBOSE=LLVERBOSE)

! * Group SFL=SFLUX

IF(ASSOCIATED(YSD_SFL%YSFL)) DEALLOCATE(YSD_SFL%YSFL)
ALLOCATE(YSD_SFL%YSFL(JPMAXSFLDS))
CALL INI_SFLP2(YSD_SFLD,YSD_SFL%YSFL,.FALSE.,'SFLUX - SD_SFL  ')
IF((.NOT.LECMWF).AND.LEDKF) THEN
  DO JFLD=1,NSFLUX
    CALL SETUP_SFLP2(YSD_SFLD,YSD_SFL%YSFL(JFLD),CDNAME=CLSFLUX)
  ENDDO
ENDIF
CALL FINALISE_SFLP2(YDDYNA,YDSURF,YSD_SFLD,YSD_SFL%YSFL,LDVERBOSE=LLVERBOSE)

! * Group SFO=SFORC

IF(ASSOCIATED(YSD_SFO%YSFO)) DEALLOCATE(YSD_SFO%YSFO)
ALLOCATE(YSD_SFO%YSFO(JPMAXSFLDS))
CALL INI_SFLP2(YSD_SFOD,YSD_SFO%YSFO,.FALSE.,'SFORC - SD_SFO  ')
IF((.NOT.LECMWF).AND.LSFORCS) THEN
  DO JFLD=1,NSFORC
    WRITE(CLSFORC,FMT='(''SURFFORC'',I4.4)') JFLD
    CALL SETUP_SFLP2(YSD_SFOD,YSD_SFO%YSFO(JFLD),CDNAME=CLSFORC)
  ENDDO
ENDIF
CALL FINALISE_SFLP2(YDDYNA,YDSURF,YSD_SFOD,YSD_SFO%YSFO,LDVERBOSE=LLVERBOSE)

!-------------------------------------------------------------------------

!*      3.  ADDITIONAL CHECKINGS AND PRINTINGS.
!           -----------------------------------

IF (LECMWF) THEN
ELSE
  IF ((LMPHYS.AND.LSOLV).AND.NCONF /= 923) THEN
    IF ( YSD_VVD%NUMFLDS < 7.OR.YSP_SBD%NLEVS < 1.OR.&
     & YSP_SBD%NUMFLDS < 2.OR.YSP_RRD%NUMFLDS < 3 ) THEN
      CALL ABOR1('SU_SURF_FLDS: LSOLV INCONSISTENT WITH YOMDPHY ')
    ENDIF
    IF (LFGEL.AND.(YSP_SBD%NUMFLDS < 3)) THEN
      CALL ABOR1('SU_SURF_FLDS: LFGEL INCONSISTENT WITH YOMDPHY ')
    ENDIF
    IF (LFGEL.AND.(YSP_RRD%NUMFLDS < 4)) THEN
      CALL ABOR1('SU_SURF_FLDS: LFGEL INCONSISTENT WITH YSP_RRD%NUMFLDS   ')
    ENDIF
    IF (LSNV) THEN
      IF (YSP_SGD%NUMFLDS < 3.OR.YSD_VFD%NUMFLDS < 10) THEN
        CALL ABOR1('SU_SURF_FLDS: LSNV INCONSISTENT WITH YOMDPHY ')
      ENDIF
      IF(.NOT.LSOLV .OR. .NOT.LNEIGE ) THEN
        CALL ABOR1('SU_SURF_FLDS: LSNV INCONSISTENT WITH LSOLV OR LNEIGE')
      ENDIF
    ENDIF
    IF (LVGSN.AND.(YSD_VVD%NUMFLDS < 10)) THEN
      CALL ABOR1('SU_SURF_FLDS: LVGSN INCONSISTENT WITH YSD_VVD%NUMFLDS  ')
    ENDIF
  ENDIF
ENDIF

WRITE(NULOUT,*) ' ----- Some printings in SU_SURF_FLDS: '
WRITE(NULOUT,*) 'NSURF=',NSURF,' NSURFL=',NSURFL,' NDIMSURF=',NDIMSURF, ' NDIMSURFL=',NDIMSURFL
WRITE(NULOUT,'('' NFPCLI = '', I2,'' NFPSWI = '', I2,'' NSATSIM = '', I2)') NFPCLI, NFPSWI, NSATSIM
NGP5 = NOFFTRAJ
NTRAJ_CST = NOFFTRAJ_CST
NGP5_OOPS = NOFFTRAJ
NTRAJ_CST_OOPS = NOFFTRAJ_CST

WRITE(NULOUT,*) 'NGP5=',NGP5,' NTRAJ_CST=',NTRAJ_CST
WRITE(NULOUT,'(A,(10I4))') ' NSTRAJGRIB ',NSTRAJGRIB(1:NGP5)

!     ------------------------------------------------------------------
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('SU_SURF_FLDS',1,ZHOOK_HANDLE)

END SUBROUTINE SU_SURF_FLDS
