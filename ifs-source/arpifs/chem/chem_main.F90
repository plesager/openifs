! initialise zbudr to prevent fpe in openifs-test
ZBUDR(:,:,:) = 0.0_JPRB
!
SELECT CASE (TRIM(CHEM_SCHEME))
  CASE ("n2o")

    ZOUT = 0.0_JPRB

    CALL CHEM_N2O (YDMODEL%YRML_GCONF,YDMODEL%YRML_PHY_EC%YREPHY,YDMODEL%YRML_CHEM,YDMODEL%YRML_PHY_MF%YRPHY2, &
