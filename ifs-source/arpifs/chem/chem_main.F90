        ZCON(JL,JK,JT)  =  PGFL(JL,JK,YCHEM(JT)%MP9_PH)
        ZTENC1(JL,JK,JT)  =  0.0_JPRB
        ZTENC0(JL,JK,JT)  =  0.0_JPRB
      ENDDO
    ENDDO
  ENDIF
