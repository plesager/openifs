SUBROUTINE GET_2DSPECIES(SELF)
IMPLICIT NONE
! ARGUMENTS
! ------------
TYPE(TEAERC_DUST), INTENT(INOUT) :: SELF

! Expected dimension lengths for month, latitude and longitude
INTEGER(KIND=JPIM), PARAMETER :: IMONTH = 12
INTEGER(KIND=JPIM), PARAMETER :: ILAT   = 180 !61
INTEGER(KIND=JPIM), PARAMETER :: ILON   = 360 !120
INTEGER, PARAMETER :: idx_SOILPH1 = 1
INTEGER, PARAMETER :: idx_SOILPH2 = 2
INTEGER, PARAMETER :: idx_SOILPH3 = 3
INTEGER, PARAMETER :: idx_SOILPH4 = 4
INTEGER, PARAMETER :: idx_SOILPH5 = 5
INTEGER, PARAMETER :: idx_Z0AM = 6
INTEGER, PARAMETER :: idx_POTSRC = 7
INTEGER, PARAMETER :: idx_SOILTYPE = 8
INTEGER, PARAMETER :: idx_AREA = 9
INTEGER, PARAMETER :: idx_CULT = 10
!3D SPECIES 
INTEGER, PARAMETER :: idx_FPAR = 1
INTEGER, PARAMETER :: idx_Z0M = 2
! Assuming dimensions have been set for lat and lon
IF (.NOT. ALLOCATED(SELF%SPECIES3D)) THEN
    ALLOCATE(SELF%SPECIES3D(SELF%NLON, SELF%NLAT, 10))
END IF
! Assuming dimensions have been set for lat and lon
IF (.NOT. ALLOCATED(SELF%SPECIES4D)) THEN
    ALLOCATE(SELF%SPECIES4D(SELF%NLON, SELF%NLAT, SELF%NMONTH, 2))
END IF
! Populate the 3D array with existing 2D data
SELF%SPECIES3D(:,:, idx_SOILPH1) = SELF%SOILPH1(:,:) !1
SELF%SPECIES3D(:,:, idx_SOILPH2) = SELF%SOILPH2(:,:) !2
SELF%SPECIES3D(:,:, idx_SOILPH3) = SELF%SOILPH3(:,:) !3
SELF%SPECIES3D(:,:, idx_SOILPH4) = SELF%SOILPH4(:,:) !4
SELF%SPECIES3D(:,:, idx_SOILPH5) = SELF%SOILPH5(:,:) !5
SELF%SPECIES3D(:,:, idx_Z0AM)    = SELF%Z0AM(:,:)    !6
SELF%SPECIES3D(:,:, idx_POTSRC)  = SELF%POTSRC(:,:)  !7
SELF%SPECIES3D(:,:, idx_SOILTYPE)= SELF%SOILTYPE(:,:)!8 
SELF%SPECIES3D(:,:, idx_AREA)= SELF%AREA(:,:)        !9
SELF%SPECIES3D(:,:, idx_CULT)= SELF%CULT(:,:)        !10 
