  !NOT-USED-NOR-ALLOCATED  SELF%TZ0AM(:) = IZ0AM(:)
  
  CALL INTP_SOILT(SELF, SELF%POTSRC, KSTART, KEND, KCOL, PLAT(KSTART:KEND), PLON(KSTART:KEND), IPOTSRC)
  !NOT-USED-NOR-ALLOCATED  SELF%TPOTSRC(:) = IPOTSRC(:)
  
