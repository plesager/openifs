  !NOT-USED-NOR-ALLOCATED  SELF%TCULT(:) = ICULT(:)
  
  CALL INTP_SOILT(SELF, SELF%LAI_MAX, KSTART, KEND, KCOL, PLAT(KSTART:KEND), PLON(KSTART:KEND), ILAI_MAX)
  !NOT-USED-NOR-ALLOCATED  SELF%TLAI_MAX(:) = ILAI_MAX(:)
  
  CALL INTP_SOILT(SELF, SELF%LAI_AVG, KSTART, KEND, KCOL, PLAT(KSTART:KEND), PLON(KSTART:KEND), ILAI_AVG)
  !NOT-USED-NOR-ALLOCATED  SELF%TLAI_AVG(:) = ILAI_AVG(:)
  
  CALL INTP_SOILT(SELF, SELF%FPAR(:,:,IM0), KSTART, KEND, KCOL, PLAT(KSTART:KEND), PLON(KSTART:KEND), IFPAR)
  !NOT-USED-NOR-ALLOCATED  SELF%TFPAR(:) = IFPAR(:)
  
  CALL INTP_SOILT(SELF, SELF%Z0M(:,:,IM0), KSTART, KEND, KCOL, PLAT(KSTART:KEND), PLON(KSTART:KEND), IZ0M)
  !NOT-USED-NOR-ALLOCATED  SELF%TZ0M(:) = IZ0M(:)

  IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS main call',1,ZHOOK_HANDLE)

