SUBROUTINE CALC_INTERP_DUST(SELF, FIELD2D, KSTART, KEND, KCOL, PLAT, PLON, INTEROUT)

  USE YOMLUN    , ONLY : NULERR
  USE YOMLUN    , ONLY : NULOUT
  USE YOMHOOK   , ONLY : LHOOK, DR_HOOK, JPHOOK
  !USE, INTRINSIC :: IEEE_ARITHMETIC
  LOGICAL :: LPROBLEM
  REAL(KIND=JPRB) :: BIG_VAL = HUGE(1.0_JPRB), SMALL_VAL = TINY(1.0_JPRB)
  CLASS(TEAERC_DUST), INTENT(INOUT):: SELF
  
  REAL(KIND=JPRB),    INTENT(IN) :: FIELD2D(:,:)
  INTEGER(KIND=JPIM), INTENT(IN) :: KCOL
  INTEGER(KIND=JPIM), INTENT(IN) :: KSTART, KEND
  REAL(KIND=JPRB),  INTENT(OUT) :: INTEROUT(KSTART:KEND)
  ! Sine of latitude of input columns
  REAL(KIND=JPRB) :: PSINLAT(KSTART:KEND)
  REAL(KIND=JPRB), INTENT(IN) :: PLAT(KSTART:KEND)
  REAL(KIND=JPRB), INTENT(IN) :: PLON(KSTART:KEND)
  INTEGER(KIND=JPIM), DIMENSION(KSTART:KEND) :: KLAT1, KLAT2, KLON1, KLON2
  REAL(KIND=JPRB)   , DIMENSION(KSTART:KEND) :: PLATWEIGHT, PLONWEIGHT
  INTEGER(KIND=JPIM) :: JL, JS, N2, N4, NM, JM, JA
  REAL(KIND=JPRB) :: ZLAT(KSTART:KEND)
  REAL(KIND=JPRB) :: ZAERA, ZAERB
  REAL(KIND=JPRB), DIMENSION(KSTART:KEND) :: ZAER2D
  REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
  REAL(KIND=JPHOOK) :: DENOM
  
  IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS 2D',0,ZHOOK_HANDLE)
  
  PLONWEIGHT = 0.0
  ! Longitude interpolation points and weights
  PLONWEIGHT = 1.0_JPRB + (PLON - SELF%LON1) / SELF%DLON
  KLON1 = MAX(1, MIN(INT(PLONWEIGHT), SELF%NLON))
  KLON2 = MOD(KLON1,SELF%NLON)+1 ! Correct treatment of dateline
  PLONWEIGHT = PLONWEIGHT - KLON1
  !ZLAT = ASIN(PSINLAT)
  PSINLAT=SIN(PLAT) ! SIN of rads
  ZLAT = PLAT !RADS

  PLATWEIGHT = 0.0
  PLATWEIGHT = 1.0_JPRB + (ZLAT - SELF%LAT1) / SELF%DLAT
  KLAT1 = MAX(0, MIN(INT(PLATWEIGHT), SELF%NLAT-1))
  KLAT2 = KLAT1+1
  ! Interpolation in sin(latitude)
  DO JL = KSTART, KEND
  DENOM = SIN(SELF%LAT1+(KLAT2(JL)-1)*SELF%DLAT) - SIN(SELF%LAT1+(KLAT1(JL)-1)*SELF%DLAT)
  PLATWEIGHT(JL) = (PSINLAT(JL) - SIN(SELF%LAT1 + (KLAT1(JL)-1)*SELF%DLAT)) / DENOM
  END DO
  !PLATWEIGHT = (PSINLAT - SIN(SELF%LAT1+(KLAT1-1)*SELF%DLAT)) &
  !     &     / (SIN(SELF%LAT1+(KLAT2-1)*SELF%DLAT) - SIN(SELF%LAT1+(KLAT1-1)*SELF%DLAT))
  ZAERA =0.0_JPRB
  ZAERB = 0.0_JPRB
  INTEROUT(KSTART:KEND) = 0.0_JPRB
  ! Interpolation loop
    DO JL = KSTART, KEND
    ZAERA = FIELD2D(KLON1(JL), KLAT1(JL)) + &
            PLATWEIGHT(JL) * (FIELD2D(KLON1(JL), KLAT2(JL)) - FIELD2D(KLON1(JL), KLAT1(JL)))
    ZAERB = FIELD2D(KLON2(JL), KLAT1(JL)) + &
            PLATWEIGHT(JL) * (FIELD2D(KLON2(JL), KLAT2(JL)) - FIELD2D(KLON2(JL), KLAT1(JL)))
    INTEROUT(JL) = ZAERA + PLONWEIGHT(JL) * (ZAERB - ZAERA)
    END DO

   IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS 2D',1,ZHOOK_HANDLE)
END SUBROUTINE CALC_INTERP_DUST

