SUBROUTINE CALC_3DINTERP_DUST(SELF, FIELD3D, KSTART, KEND, KCOL, PLAT, PLON, INTEROUT)
  USE YOMLUN, ONLY : NULOUT
  USE YOMHOOK   , ONLY : LHOOK, DR_HOOK, JPHOOK  
  IMPLICIT NONE
  
  CLASS(TEAERC_DUST), INTENT(INOUT) :: SELF
  REAL(KIND=JPRB), INTENT(IN) :: FIELD3D(:,:,:)
  INTEGER(KIND=JPIM), INTENT(IN) :: KSTART, KEND, KCOL
  REAL(KIND=JPRB), INTENT(IN) :: PLAT(KSTART:KEND), PLON(KSTART:KEND)
  REAL(KIND=JPRB), INTENT(OUT) :: INTEROUT(KSTART:KEND,SELF%NMONTH)
  INTEGER(KIND=JPIM), PARAMETER :: ILAT = 180, ILON = 360, IMONTH = 12
  REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
  INTEGER(KIND=JPIM) :: JL, JM
  INTEGER(KIND=JPIM), DIMENSION(KSTART:KEND) :: KLAT1, KLAT2, KLON1, KLON2
  REAL(KIND=JPRB), DIMENSION(KSTART:KEND) ::  PLATWEIGHT, PLONWEIGHT
  REAL(KIND=JPRB), DIMENSION(KSTART:KEND):: PSINLAT, ZLAT
  REAL(KIND=JPRB)    :: ZAERA, ZAERB

  IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS 3D',0,ZHOOK_HANDLE)
  ! Precompute sine of latitude and indices
  PSINLAT = SIN(PLAT)
  ZLAT = PLAT

  PLONWEIGHT = 1.0_JPRB + (PLON - SELF%LON1) / SELF%DLON
  KLON1 = MAX(1, MIN(INT(PLONWEIGHT), SELF%NLON))
  KLON2 = MOD(KLON1, SELF%NLON) + 1
  PLONWEIGHT = PLONWEIGHT - KLON1

  PLATWEIGHT = 1.0_JPRB + (ZLAT - SELF%LAT1) / SELF%DLAT
  KLAT1 = MAX(1, MIN(INT(PLATWEIGHT), SELF%NLAT - 1))
  KLAT2 = KLAT1 + 1
  PLATWEIGHT = (PSINLAT - SIN(SELF%LAT1 + (KLAT1 - 1)*SELF%DLAT)) / &
               (SIN(SELF%LAT1 + (KLAT2 - 1)*SELF%DLAT) - SIN(SELF%LAT1 + (KLAT1 - 1)*SELF%DLAT))

  ! --- Interpolate for each column across all months and average ---
    IF (    SIZE(FIELD3D,1) == ILON .OR. &
       &  SIZE(FIELD3D,2) == ILAT .OR. &
       &  SIZE(FIELD3D,3) == IMONTH) THEN
    WRITE(NULOUT,*) '3D FIELDS INTERPOLATION ...'
    ELSE 
     WRITE(NULOUT,*) '3D FIELDS INTERPOLATION was not 3d ...', FIELD3D(195,116,6)
     WRITE(NULOUT,*) '3D FIELDS LON:',SIZE(FIELD3D,1),'LAT:', SIZE(FIELD3D,2), 'MONTH:', SIZE(FIELD3D,3)
    END IF
    DO JM =1,SELF%NMONTH                           !loop months
      DO JL = KSTART,KEND                        !loop model colum
    ! Bilinear interpolation in lon/lat month  for each species3d
      ZAERA =  FIELD3D(KLON1(JL), KLAT1(JL), JM) &
                 & + PLATWEIGHT(JL) * (FIELD3D(KLON1(JL), KLAT2(JL), JM) &
            &                    - FIELD3D(KLON1(JL), KLAT1(JL), JM))
      ZAERB = FIELD3D(KLON2(JL), KLAT1(JL),  JM) &
                  & + PLATWEIGHT(JL) * (FIELD3D(KLON2(JL), KLAT2(JL), JM) &
              &                    -  FIELD3D(KLON2(JL), KLAT1(JL),  JM))
      INTEROUT(JL,JM) = ZAERA + PLONWEIGHT(JL) * (ZAERB - ZAERA)
      END DO
     END DO

     IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS 3D',1,ZHOOK_HANDLE)
END SUBROUTINE CALC_3DINTERP_DUST

SUBROUTINE INTP_SOILT(SELF,FIELD2D, KSTART, KEND,KCOL, PLAT, PLON, INTERSOILT)

  USE YOMHOOK, ONLY : LHOOK, DR_HOOK, JPHOOK
  USE YOMLUN    , ONLY : NULOUT
  REAL(KIND=JPRB),    INTENT(IN) :: FIELD2D(:,:)
  INTEGER(KIND=JPIM), INTENT(IN) :: KCOL
  CLASS(TEAERC_DUST), INTENT(IN) :: SELF
  INTEGER(KIND=JPIM), INTENT(IN) :: KSTART, KEND
  REAL(KIND=JPRB) :: PSINLAT(KSTART:KEND)
  REAL(KIND=JPRB), INTENT(IN) :: PLAT(KSTART:KEND)
  REAL(KIND=JPRB), INTENT(IN) :: PLON(KSTART:KEND)
  !REAL(KIND=JPRM), INTENT(OUT), DIMENSION(KCOL) :: INTERSOILT
  !INTEGER(KIND=JPIM), INTENT(OUT) :: INTERSOILT (KSTART:KEND)
  REAL(KIND=JPRB), INTENT(OUT) :: INTERSOILT (KSTART:KEND)
  
  INTEGER(KIND=JPIM), DIMENSION(KSTART:KEND) :: KLAT1, KLAT2, KLON1, KLON2
  REAL(KIND=JPRB), DIMENSION(KSTART:KEND) :: PLATWEIGHT, PLONWEIGHT
  INTEGER(KIND=JPIM) :: JL, JS, N2, N4, NM, JM, JA
  REAL(KIND=JPRB) :: ZLAT(KSTART:KEND)
  REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
  REAL(KIND=JPRB) :: ZAERA, ZAERB
  REAL(KIND=JPRB), DIMENSION(KSTART:KEND) :: ZAER2D
  !C. Perez
  INTEGER(KIND=JPIM) :: I_INX, J_INX
  IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS SOILTYPE',0,ZHOOK_HANDLE)

  PLATWEIGHT = 0.0
  PLONWEIGHT = 0.0
  ! Longitude interpolation points and weights
  PLONWEIGHT = 1.0_JPRB + (PLON - SELF%LON1) / SELF%DLON
  KLON1 = MAX(1, MIN(INT(PLONWEIGHT), SELF%NLON))
  KLON2 = MOD(KLON1,SELF%NLON)+1 ! Correct treatment of dateline
  PLONWEIGHT = PLONWEIGHT - KLON1

  !ZLAT = ASIN(PSINLAT)
  PSINLAT=SIN(PLAT) ! SIN of rads
  ZLAT = PLAT !RADS
  PLATWEIGHT = 1.0_JPRB + (ZLAT - SELF%LAT1) / SELF%DLAT
!  KLAT1 = MAX(0, MIN(INT(PLATWEIGHT), SELF%NLAT-1))  !C. Perez: Why 0? Does the latitude indexing start in 0? 
  KLAT1 = MAX(1, MIN(INT(PLATWEIGHT), SELF%NLAT-1))
  KLAT2 = KLAT1+1

  ! Interpolation in sin(latitude)
  PLATWEIGHT = (PSINLAT - SIN(SELF%LAT1+(KLAT1-1)*SELF%DLAT)) &
       &     / (SIN(SELF%LAT1+(KLAT2-1)*SELF%DLAT) - SIN(SELF%LAT1+(KLAT1-1)*SELF%DLAT))

  DO JL = KSTART,KEND
  !C.Perez
  !choose the closer longitude corner
  IF (PLONWEIGHT(JL) > 0.5_JPRB) THEN
      I_INX = KLON2(JL)
    ELSE
      I_INX = KLON1(JL)
    END IF
   !choose the closer latitude  corner
  IF (PLATWEIGHT(JL) > 0.5_JPRB) THEN
      J_INX = KLAT2(JL)
    ELSE
      J_INX = KLAT1(JL)
    END IF
  
   INTERSOILT(JL) = FIELD2D(I_INX, J_INX)
   !C. Perez
  END DO
  IF (LHOOK) CALL DR_HOOK('YOEDUST:INTERPOLATION FIELDS SOILTYPE',1,ZHOOK_HANDLE)
END SUBROUTINE INTP_SOILT

